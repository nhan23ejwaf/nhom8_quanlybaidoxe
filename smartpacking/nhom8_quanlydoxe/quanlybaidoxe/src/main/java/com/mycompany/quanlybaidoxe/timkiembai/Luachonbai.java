    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    User user = User.getInstance();
    String tk = jTextField1.getText();
    int gio = Integer.parseInt((String) jComboBox1.getSelectedItem());
    int phut = Integer.parseInt((String) jComboBox2.getSelectedItem());

    if (tk.equals("") || (gio == 0 && phut == 0) || user.getTrangThaiXe()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập thông tin hợp lệ hoặc xe đã có trong bãi.");
    } else {
        // Kiểm tra nếu table1 không có dữ liệu
        if (table1.getRowCount() == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Không có dữ liệu bãi đỗ xe để cập nhật.");
            return; // Dừng thực hiện nếu không có dữ liệu
        }
        
        String filePath = "data_parkSpot.xml";
        String filePath1 = "data_Customer.xml";
        
        try {
            UpdateParkingSpot updater = new UpdateParkingSpot(filePath);
            updater.updateSpot(tk, user.getName(), true);

            UserUpdater userUpdater = new UserUpdater(filePath1);
            user.setGio(String.valueOf(gio));
            user.setPhut(String.valueOf(phut));
            user.setSpotId(tk);
            user.setTrangThaiXe(true);
            userUpdater.updateUser(user);

            this.dispose();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Lỗi trong quá trình cập nhật dữ liệu: " + e.getMessage());
        }
    }        
        
    }//GEN-LAST:event_jButton1ActionPerformed

